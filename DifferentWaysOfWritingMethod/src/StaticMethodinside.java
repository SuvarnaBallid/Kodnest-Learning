//Static Method inside the class
public class StaticMethodinside {
	public static void main(String[]args) {
		int a=60;
		int b=40;
		int res=add(a,b);//Method call
		System.out.println(res);
	}
	public static int add(int a,int b)
	{
		int c=a+b;
		return c;
	}	
}
